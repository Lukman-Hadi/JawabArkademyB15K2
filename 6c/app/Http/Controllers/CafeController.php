     */
    public function store(Request $request)
    {
        $product= new Product;
        $product->nama = $request->product_name;
        $product->price = $request->price;
        $product->id_cashier = $request->id_c;
        $product->id_category = $request->id_cat;
        $product->save();
        return back();
    }

